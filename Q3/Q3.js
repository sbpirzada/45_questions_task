"use strict";
// Name Cases: Store a person’s name in a variable, and then print that person’s name in lowercase, uppercase, and titlecase.
let personname = "Kamran Tessori";
console.log(personname.toLowerCase()); // Shows the name in all small letters
console.log(personname.toUpperCase()); // Shows the name in ALL BIG LETTERS
console.log(personname.charAt(0).toUpperCase() + personname.slice()); // Shows the name with the First Letter Big.
